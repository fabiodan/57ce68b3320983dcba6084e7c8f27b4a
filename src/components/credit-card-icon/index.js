const CreditCardIconBase = ({
  children, className, color, size,
}) => {
  const modifiers = [
    !!size && `credit-card-icon--${size}`,
  ]
  const classNames = joinClassNames('credit-card-icon', className, modifiers)

  return (
    <span className={classNames}>
      {children}
    </span>
  )
}

